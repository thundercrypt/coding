# This program gives the capital of the country entered by the user.
userCountry = str(input("Please enter a Country Name: "))

if(userCountry==India): print("The capital of",userCountry,"is New Delhi")
elif(userCountry==Australia): print("The capital of",userCountry,"is Canberra")
elif(userCountry==France): print("The capital of",userCountry,"is Paris")
elif(userCountry==Japan): print("The capital of",userCountry,"is Tokyo")
else: print("Please enter a country included in the database.")
