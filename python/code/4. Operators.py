# Arithmetic Operators
# Let's take any two variables and perform arithmetic operations on them.
x = 3
y = 4
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x**y)
print(x%y)
print(x//y)

# Now let's take the same two variables and use comparision operators on them.
if(x==y): print("x=7")
if(x>=y): print("x is greater than or equal to y")
if(x>y): print("x is greater than y")
if(x!=y): print("x is not equal to y")
if(x<=y): print("x is less than or equal to y")
if(x<y): print("x is less than y")
